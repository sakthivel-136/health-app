        if st.button("Calculate BMI"):
            if height > 0:
                bmi = weight / (height ** 2)
                st.success(f"Your BMI is: {bmi:.2f}")
                if bmi < 18.5:
                    st.info("You are underweight.")
                elif 18.5 <= bmi < 24.9:
                    st.success("You have a normal weight.")
                elif 25 <= bmi < 29.9:
                    st.warning("You are overweight.")
