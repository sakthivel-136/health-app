        if st.button("Check Hydration"):
            recommended = weight * 0.033
            st.info(f"Recommended: {recommended:.2f} L/day")
            if water_intake >= recommended:
                st.success("You're well hydrated!")
            else:
                st.warning("You need to drink more water.")

